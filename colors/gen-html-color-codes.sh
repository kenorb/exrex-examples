#!/usr/bin/env sh
# Generate HTML Color Codes
# Count: 913376317426246207576266578071032499473532971233162208768439305656173952
# Examples:
# - #FF0000
# - #0A4
# - rgb(255,0,0)
# - rgba(255,0,0,0.5)
# - hsl(120,100%,50%)

# Define color formats
HEX_LONG="#[0-9a-fA-F]{6}"
HEX_SHORT="#[0-9a-fA-F]{3}"
RGB="rgb\((1?[0-9]{1,2}|2[0-4][0-9]|25[0-5]),\s*(1?[0-9]{1,2}|2[0-4][0-9]|25[0-5]),\s*(1?[0-9]{1,2}|2[0-4][0-9]|25[0-5])\)"
RGBA="rgba\((1?[0-9]{1,2}|2[0-4][0-9]|25[0-5]),\s*(1?[0-9]{1,2}|2[0-4][0-9]|25[0-5]),\s*(1?[0-9]{1,2}|2[0-4][0-9]|25[0-5]),\s*(0(\.[0-9]+)?|1(\.0+)?)\)"
HSL="hsl\((3[0-5][0-9]|[12][0-9][0-9]|[1-9]?[0-9]),\s*(100|[1-9]?[0-9])%,\s*(100|[1-9]?[0-9])%\)"

exrex $* \
    "$HEX_LONG|$HEX_SHORT|$RGB|$RGBA|$HSL"